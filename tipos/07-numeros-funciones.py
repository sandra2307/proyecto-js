# modulo = archivo con codigo escrito que podemos traer
import math  # math es el modulo con el que vamos a empezar a trabajar
# math contiene math.py contiene codigo escrito en python que se encuentra en otro archivo y podemos usar

print(round(1.3))  # round devuelve el número al que más cercano se encuentre 1.3
print(round(1.8))  # este round nos devolveria el numero 2 porque está más cerca de 2
print(round(1.5))  # el 5 nos devuelve el 2

print(abs(-77))  # valor absoluto del numero. nos devolveria 77
print(abs(55))  # nos devuelve 55

print(math.ceil(1.1))  # imprime 2 porque es el numero mas cercano por arriba
# imprime 1 porque es el numero entero mas cercano por abajo
print(math.floor(1.9999))
print(math.isnan(23))  # isnan solo sirve si pasamos numeros reales
# print(math.isnan("23")) al ser un string nos devolveria un error
print(math.pow(10, 3))  # potencia, eleva 10 a 3
print(math.sqrt(9))  # saca la raiz cuadrada del numero que le pones

